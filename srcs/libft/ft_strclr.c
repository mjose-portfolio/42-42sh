#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	i = 0;
	if ((char *)s == NULL)
		return ;
	while (((char *)s)[i])
	{
		((char *)s)[i] = '\0';
		i++;
	}
}
